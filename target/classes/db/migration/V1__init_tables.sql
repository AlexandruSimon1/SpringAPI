CREATE TABLE administrators(
ADMINISTRATOR_ID INT PRIMARY KEY AUTO_INCREMENT,
FIRST_NAME VARCHAR(30),
LAST_NAME VARCHAR(30),
DATE_OF_BIRTH DATE,
ADDRESS VARCHAR(30),
PHONE_NUMBER BIGINT,
EMAIL VARCHAR(30)
);

CREATE TABLE waiters(
WAITER_ID INT PRIMARY KEY AUTO_INCREMENT,
FIRST_NAME VARCHAR(30),
LAST_NAME VARCHAR(30),
DATE_OF_BIRTH DATE,
ADDRESS VARCHAR(30),
PHONE_NUMBER BIGINT,
EMAIL VARCHAR(30)
);

CREATE TABLE menus(
PRODUCT_ID INT PRIMARY KEY AUTO_INCREMENT,
CATEGORY VARCHAR(30),
NAME VARCHAR(30),
DESCRIPTION VARCHAR(100),
PRICE DECIMAL(19,2)
);

CREATE TABLE tables(
TABLE_ID INT PRIMARY KEY AUTO_INCREMENT,
NUMBER INT
);

CREATE TABLE orders(
ORDER_ID INT PRIMARY KEY AUTO_INCREMENT,
ORDER_NUMBER INT,
QUANTITY INT,
PRODUCT_ID INT,
TABLE_ID INT,
FOREIGN KEY (PRODUCT_ID) REFERENCES menus(PRODUCT_ID),
FOREIGN KEY (TABLE_ID) REFERENCES tables(TABLE_ID)
);

CREATE TABLE checkout(
CHECK_OUT_ID INT PRIMARY KEY AUTO_INCREMENT,
PAYMENT_TYPE VARCHAR(30),
ORDER_ID INT,
FOREIGN KEY (ORDER_ID) REFERENCES orders(ORDER_ID)
);

CREATE TABLE ORDERPRODUCTS(
ORDERS_ORDER_ID INT,
MENUS_PRODUCT_ID INT,
CONSTRAINT FOREIGN KEY (ORDERS_ORDER_ID)references orders(ORDER_ID),
CONSTRAINT FOREIGN KEY (MENUS_PRODUCT_ID)references menus(PRODUCT_ID)
);